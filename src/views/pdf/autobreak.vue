<script setup lang="ts">
import { ref, nextTick } from 'vue'
import html2Canvas from 'html2canvas'
import JsPDF from 'jspdf'

const pdfRef = ref<HTMLElement | null>(null)
const loading = ref(false)

const exportPdf = async () => {
  if (!pdfRef.value) return
  loading.value = true
  try {
    await nextTick()
    const canvas = await html2Canvas(pdfRef.value, {
      scale: 2,
      useCORS: true,
      logging: false,
      backgroundColor: '#ffffff',
    })
    const contentWidth = canvas.width
    const contentHeight = canvas.height
    const a4Width = 595.28
    const a4Height = 841.68
    const imgWidth = a4Width - 36
    const imgHeight = (a4Width / contentWidth) * contentHeight
    const pageData = canvas.toDataURL('image/jpeg', 1.0)

    const pdf = new JsPDF('', 'pt', 'a4')
    const pageCount = Math.ceil(imgHeight / a4Height)

    if (pageCount <= 1) {
      pdf.addImage(pageData, 'JPEG', 18, 0, imgWidth, imgHeight)
    } else {
      for (let i = 0; i < pageCount; i++) {
        if (i > 0) {
          pdf.addPage()
        }
        pdf.addImage(pageData, 'JPEG', 18, -i * a4Height, imgWidth, imgHeight)
      }
    }
    pdf.save('report.pdf')
  } catch (error) {
    console.error('PDF export error:', error)
  } finally {
    loading.value = false
  }
}

